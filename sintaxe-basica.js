// statements terminam com ponto e virgula
var x = 15;
var y = 12;

// Tipos de valores que existem no JavaScript.
// O JavaScript conta com dois tipos de valores: literais e variaveis.
/** 
 * Os valores literais sao aqueles que sao fixos e os valores variaveis variam de acordo
 * com nossa necessidade.
 */

 /** Valores Literais: Numeros: 10, 10.5. Cadeias de caracteres (strings): 'Olah mundo' */

 /** Variaveis. Usam a palavra-chave var. Com essa palavra-chave, o compilador vai entender que
  * estamos declarando um valor variavel.
  */

  /** x eh um valor variavel e podemos atribuir algum outro valor a ele. */
  var x;
  
  /** Para atribuir um valor, antes eh necessario aprender sobre os operadores no JavaScript. */

  /** Operadores: sao sinais que agregam acoes aos nossos valores. */

  /** Operador = (igual). Esse operador vai agregar um valor a variavel x. */
   
  x = 10;

  /** Operadores aritmeticos */

  x = 10 + 5;
  x = 20 - 10;
  x = 40 * 5;
  x = 20 / 10;

  /** Quando juntamos operadores e valores, temos expressoes. 
   * As expressoes podem conter tanto valores literais quanto valores variaveis.
  */

  /*** Expressoes */
  x = y + 10;
  x = 20 * 30;
  x = x * y;

  // Comentarios 2 barras: para comentar uma linha.

  /** Comentarios em bloco significa que pode definir varias linhas de comentario. */   

/** Identificadores: sao os nomes possiveis para uma variavel no JavaScript. Normalmente, os 
 * identificadores, no JavaScript, sao feitos em uma formatacao que chamamos de CamelCase.
 */
// variavel feita em CamelCase.
var sintaxeBasica;

